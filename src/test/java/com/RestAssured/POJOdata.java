package com.RestAssured;

public class POJOdata {

//Encapsulation = data + code (function)
	private String name;
	private String job;

	// getter methods
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	// setter methods
	public String getJob() {
		return job;
	}

	public void setJob(String job) {
		this.job = job;
	}

}
